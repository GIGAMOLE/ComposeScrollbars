/**
 * The scrollbars content orientation for [ScrollbarsConfig].
 *
 * @see ScrollbarsScrollType
 * @see ScrollbarsVisibilityType
 * @author GIGAMOLE
 */
enum class ScrollbarsOrientation {

    /** The scrollbars orientation for a vertical scroll content. */
    Vertical,

    /** The scrollbars orientation for a horizontal scroll content. */
    Horizontal
}